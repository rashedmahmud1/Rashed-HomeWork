package com.tt.Sohel;

public class BKArraySum {

	public static void main(String[] args) {
		int sohel[] = {23,34,45,56,67};
		int sum = 0;
		for(int counter=0;counter<sohel.length;counter++) {
			sum+=sohel[counter];
			System.out.println("this is sum of these numbers     "+ sum);
		}
		

	}//end of main method

}//end of class
