package com.tt.Sohel;

public class arraysSum {

	public static void main(String[] args) {
		int sohel[] = { 45,55,67,80};
		int sum = 0;
		for(int counter=0;counter<sohel.length;counter++) {
			sum+=sohel[counter];
		}
		System.out.println("this is the total of arrays :"+sum);
		

	}//end of main method

}//end of class
