package com.tt.Sohel;

public class BKArrayTable {
	
public static void main(String[] args) {
		System.out.println("index   \t   Value");
		int sohel[]={9,53,01,17,82};
		
		for(int counter=0;counter<sohel.length;counter++) {
			System.out.println(counter+ "\t" +sohel[counter]);
		}

	}//end of main method

}//end of class
